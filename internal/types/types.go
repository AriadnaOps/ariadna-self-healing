// DetectionState tracks the state of a detection per scenario+resource
// This is in-memory state used by Detection Engine for threshold counting
//
// Example: Scenario "CrashLoopBackOff" needs "3 crashes in 5 minutes"
// DetectionState tracks: first crash time, count, whether threshold is met
//
// SCHEMA-DRIVEN: Threshold configuration comes from ScenarioLibrary CRD:
//
//	detection:
//	  threshold:
//	    count: 3
//	    window: 15m
type DetectionState struct {
	// ScenarioID is the scenario this state is for
	// Key component: state is per-scenario
	ScenarioID string `json:"scenarioId"`

	// Resource is the resource this state is for
	// Key component: state is per-resource
	// Together: ScenarioID + Resource = unique state key
	Resource ResourceReference `json:"resource"`

	// FirstDetected is when the condition was first detected
	// Marks the start of the counting window
	FirstDetected time.Time `json:"firstDetected"`

	// LastDetected is the most recent detection
	// Updated each time condition matches
	LastDetected time.Time `json:"lastDetected"`

	// DetectionCount is the number of detections in the current window
	// Example: if threshold is "3 in 5 minutes", this counts up to 3
	DetectionCount int `json:"detectionCount"`

	// ThresholdMet indicates if the threshold has been met
	// true = ready to emit DetectionResult to remediation layer
	ThresholdMet bool `json:"thresholdMet"`

	// WindowStart is the start of the current counting window
	// Used to expire old detections: if now - WindowStart > scenario's threshold window, reset.
	// The window duration is defined on the scenario (detection.ThresholdConfig.Window or
	// DetectionSpec.Threshold.Window in the CRD), not on this state.
	WindowStart time.Time `json:"windowStart"`
}

// RemediationState tracks the state of remediation per scenario+resource
// This is in-memory state used by Remediation Controller for cooldown/retry tracking
//
// Example: After restarting a pod, don't restart again for 15 minutes (cooldown)
// RemediationState tracks: last action time, retry count, cooldown expiry
//
// SCHEMA-DRIVEN: Cooldown configuration comes from config or ScenarioLibrary:
//
//	remediation:
//	  cooldown: 15m
//	  maxAttempts: 3
type RemediationState struct {
	// ScenarioID is the scenario this state is for
	ScenarioID string `json:"scenarioId"`

	// Resource is the resource this state is for
	Resource ResourceReference `json:"resource"`

	// LastRemediation is when the last action was executed
	// Used to calculate time since last action (for cooldown check)
	LastRemediation time.Time `json:"lastRemediation"`

	// RemediationCount is the number of consecutive remediation attempts
	// Increments on each attempt, resets on success or after long period
	// Used for: "if tried 3 times and still failing, escalate"
	RemediationCount int `json:"remediationCount"`

	// CooldownUntil is when the cooldown expires
	// Set after each remediation: CooldownUntil = now + cooldown duration
	// Prevents "remediation storms" (repeated actions too quickly)
	CooldownUntil time.Time `json:"cooldownUntil"`

	// Escalated indicates if this has been escalated
	// Once escalated, stop auto-remediation (needs human intervention)
	Escalated bool `json:"escalated"`

	// LastActionStatus is the status of the last action
	// Helps decide: if last action failed, maybe try different action
	LastActionStatus ActionStatus `json:"lastActionStatus"`
}

// ============================================================================
// UTILITY FUNCTIONS
// ============================================================================

// StateKey generates a unique key for state storage
// Used by both DetectionState and RemediationState maps
//
// Parameters:
//
//	scenarioID - The scenario identifier (from ScenarioLibrary)
//	resource - The Kubernetes resource reference
//
// Returns:
//
//	String key in format: "scenarioID/Kind/Namespace/Name"
//
// This is a PACKAGE-LEVEL FUNCTION (not a method on a type)
// Called as: types.StateKey(id, ref)
func StateKey(scenarioID string, resource ResourceReference) string {
	// Combine scenario ID with resource key
	// Ensures state is isolated per scenario+resource combination
	// Different scenarios can track the same resource independently
	return scenarioID + "/" + resource.Key()
}

