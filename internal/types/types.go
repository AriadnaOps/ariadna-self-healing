// ActionType represents the type of remediation action
// These are the "verbs" the operator can perform
// Handler Pattern: each type has a corresponding handler implementation
//
// ActionType is the kind of remediation action (each has a handler in the action package).
type ActionType string

const (
	// ActionTypeScaleUp increases deployment replicas
	// Use case: High load, high CPU/memory, increased traffic
	ActionTypeScaleUp ActionType = "scaleUp"

	// ActionTypeScaleDown decreases deployment replicas
	// Use case: Overprovisioned, low load, cost optimization
	ActionTypeScaleDown ActionType = "scaleDown"

	// ActionTypeRestartPod deletes pod to trigger restart
	// Use case: Stuck pod, memory leak, transient errors
	ActionTypeRestartPod ActionType = "restartPod"

	// ActionTypeRollbackToPrevious rolls back to previous deployment revision
	// Use case: New deployment causing crashes, bugs in new version
	ActionTypeRollbackToPrevious ActionType = "rollback"

	// ActionTypeAdjustCPU modifies CPU limits/requests
	// Use case: CPU throttling, optimize resource allocation
	ActionTypeAdjustCPU ActionType = "adjustCPU"

	// ActionTypeAdjustMemory modifies memory limits/requests
	// Use case: OOMKilled, memory pressure, right-sizing
	ActionTypeAdjustMemory ActionType = "adjustMemory"

	// ActionTypeUpdateConfig updates ConfigMap values
	// Use case: Dynamic configuration changes for recovery
	ActionTypeUpdateConfig ActionType = "updateConfig"

	// ActionTypeRunJob creates a Kubernetes Job
	// Use case: Run cleanup script, database migration, repair task
	ActionTypeRunJob ActionType = "runJob"

	// ActionTypeNotify sends notification to external systems
	// Use case: Alert on-call, send to Slack/PagerDuty, create ticket
	ActionTypeNotify ActionType = "notify"
)

// ActionConfig defines a remediation action to execute
// This is the "instruction" for what action to take and how
//
// SCHEMA-DRIVEN: Actions defined in ScenarioLibrary CRD:
//
//	remediation:
//	  actions:
//	  - type: rollback
//	    order: 1
//	    params:
//	      condition: "hasRecentDeployment()"
//	  - type: notify
//	    order: 2
//	    params:
//	      severity: critical
type ActionConfig struct {
	// Type is the action type (which handler to use)
	Type ActionType `json:"type"`

	// Order determines execution order (lower = first)
	// Allows sequential execution: notify first, then rollback, then scale
	// Same order number = can execute in parallel
	Order int `json:"order"`

	// Params contains action-specific parameters
	// map[string]interface{} allows flexible parameters per action type
	//
	// Examples:
	//   scaleUp: {"increment": 2, "maxReplicas": 10}
	//   adjustMemory: {"increase": "25%", "maxValue": "2Gi"}
	//   notify: {"severity": "high", "channels": ["slack", "pagerduty"]}
	//   rollback: {"condition": "deploymentAge < 1h"}
	Params map[string]interface{} `json:"params,omitempty"`

	// Conditions are optional CEL conditions for execution
	// Example: only rollback if deployment is less than 1 hour old
	// Allows conditional logic: "if recent deployment then rollback else restart"
	//
	// CEL expression examples:
	//   - "hasRecentDeployment(resource)" - check deployment age
	//   - "resource.metadata.labels['canary'] == 'true'" - only canary pods
	//   - "detectionCount > 5" - only after multiple detections
	Conditions []ActionCondition `json:"conditions,omitempty"`
}

// ActionCondition defines a CEL condition for action execution
// Allows if/unless logic for smarter remediation
//
// SCHEMA-DRIVEN: Conditions evaluated at runtime via CEL
type ActionCondition struct {
	// Type is the condition type: "if" or "unless"
	// "if": execute action only if expression evaluates to true
	// "unless": execute action only if expression evaluates to false
	Type string `json:"type"`

	// Expression is the CEL condition expression
	// Has access to variables:
	//   - resource: ResourceReference of affected resource
	//   - detectionCount: number of times detected
	//   - context: additional context data
	//
	// Examples:
	//   - "deploymentAge(resource) < duration('1h')" - recent deployment
	//   - "detectionCount > 5" - many detections
	//   - "resource.metadata.labels['env'] == 'prod'" - production only
	Expression string `json:"expression"`
}

// ActionStatus represents the status of an action execution
// Tracks the lifecycle of an action attempt
type ActionStatus string

const (
	ActionStatusPending   ActionStatus = "pending"   // Queued, not started yet
	ActionStatusRunning   ActionStatus = "running"   // Currently executing
	ActionStatusSucceeded ActionStatus = "succeeded" // Completed successfully
	ActionStatusFailed    ActionStatus = "failed"    // Failed to execute
	ActionStatusSkipped   ActionStatus = "skipped"   // Skipped (cooldown, condition)
	ActionStatusDryRun    ActionStatus = "dryRun"    // Dry-run mode (logged, not executed)
)

// ActionResult is the result of executing a remediation action
// This is the "receipt" of what was done
//
// Flow: Action Handler → ActionResult → (metrics, logs, possibly next action)
type ActionResult struct {
	// ID is a unique identifier for this result
	ID string `json:"id"`

	// DetectionResultID links back to the detection that triggered this
	// Allows tracing: DetectionInput → DetectionResult → ActionResult
	DetectionResultID string `json:"detectionResultId"`

	// ScenarioID is the scenario that was triggered
	// For metrics aggregation by scenario
	ScenarioID string `json:"scenarioId"`

	// Resource is the affected Kubernetes resource
	// What resource did we act on?
	Resource ResourceReference `json:"resource"`

	// Action is the action that was executed (includes type and params)
	// Full action config for audit trail
	Action ActionConfig `json:"action"`

	// Status is the result status (succeeded, failed, skipped, etc.)
	Status ActionStatus `json:"status"`

	// StartTime is when the action started
	// For measuring action duration
	StartTime time.Time `json:"startTime"`

	// EndTime is when the action completed
	// Duration = EndTime - StartTime
	EndTime time.Time `json:"endTime"`

	// Message is a human-readable result description
	// Examples: "Scaled deployment from 2 to 4 replicas", "Rollback completed"
	Message string `json:"message"`

	// Error contains error details if the action failed
	// Only populated if Status == ActionStatusFailed
	Error string `json:"error,omitempty"`

	// Changes describes what was changed
	// Allows tracking exact modifications for audit and rollback
	// Example: [{"field": "spec.replicas", "oldValue": "2", "newValue": "4"}]
	Changes []ResourceChange `json:"changes,omitempty"`
}

// ResourceChange describes a change made to a resource
// Provides detailed audit trail of modifications
type ResourceChange struct {
	// Field is the field that was changed
	// JSONPath-style: "spec.replicas", "spec.template.spec.containers[0].resources.limits.memory"
	Field string `json:"field"`

	// OldValue is the previous value (if known)
	// String representation for simplicity
	OldValue string `json:"oldValue,omitempty"`

	// NewValue is the new value
	// What we changed it to
	NewValue string `json:"newValue"`
}

