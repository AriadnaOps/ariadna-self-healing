// Package types defines the core data types used across the operator pipeline:
// DetectionInput (monitor → detection), DetectionResult (detection → remediation), ActionResult (remediation → metrics).
package types

import (
	"time"

	metav1 "k8s.io/apimachinery/pkg/apis/meta/v1"
	"k8s.io/apimachinery/pkg/types"
)

// DetectionSource indicates where the detection input originated (Kubernetes API or OpenTelemetry).
type DetectionSource string

const (
	DetectionSourceKubernetes DetectionSource = "kubernetes"
	DetectionSourceOTel       DetectionSource = "otel"
)

// Severity is the severity level of a detection or action (prioritization, escalation, notifications).
type Severity string

const (
	SeverityLow      Severity = "low"      // Informational, no immediate action needed
	SeverityMedium   Severity = "medium"   // Should investigate, but not urgent
	SeverityHigh     Severity = "high"     // Requires attention soon
	SeverityCritical Severity = "critical" // Immediate action required, may page on-call
)

// InstanceRole is the operator instance role when leader election is enabled (Leader runs pipeline and publishes tasks; Workers claim and execute them).
type InstanceRole string

const (
	RoleLeader InstanceRole = "leader"
	RoleWorker InstanceRole = "worker"
)

// ResourceReference identifies a Kubernetes resource (any API group/kind).
type ResourceReference struct {
	// APIVersion is the API version of the resource
	// Examples: "v1" (core), "apps/v1" (Deployment), "batch/v1" (Job)
	// For CRDs: "mycompany.io/v1" (custom resources)
	APIVersion string `json:"apiVersion"`

	// Kind is the kind of the resource
	// Examples: "Pod", "Deployment", "Service", "Node"
	// For CRDs: Any custom kind like "MyCustomResource"
	Kind string `json:"kind"`

	// Namespace is the namespace of the resource (empty for cluster-scoped)
	// Empty for cluster-scoped resources like Nodes, PersistentVolumes, CRDs
	Namespace string `json:"namespace"`

	// Name is the name of the resource
	// Must be unique within namespace+kind combination
	Name string `json:"name"`

	// UID is the unique identifier of the resource
	// Kubernetes assigns this, globally unique across cluster lifetime
	// Used to detect if a resource was deleted and recreated with same name
	UID string `json:"uid,omitempty"` // omitempty: don't serialize if empty
}

func (r ResourceReference) String() string {
	if r.Namespace != "" {
		return r.Namespace + "/" + r.Name
	}
	// Cluster-scoped resources (Nodes, PVs) just show name
	return r.Name
}

// Key returns a unique key for the resource (Kind/Namespace/Name). Uses name/namespace rather than UID so we track the same logical resource across recreates.
func (r ResourceReference) Key() string {
	return r.Kind + "/" + r.Namespace + "/" + r.Name
}

// DetectionInput is the payload from the monitor layer to the detection layer. Data holds the raw payload; scenarios match via CEL against it.
type DetectionInput struct {
	// ID is a unique identifier for this input
	// Generated by monitor layer (UUID), used for tracing/debugging
	// Example: "01J2K3M4N5P6Q7R8S9T0"
	ID string `json:"id"`

	// Source indicates where this input originated
	// One of: "kubernetes" or "otel"
	// This is a broad category - specific details in Labels and Data
	Source DetectionSource `json:"source"`

	// Resource is the Kubernetes resource this input relates to
	// Even OTel data is associated with a resource (the pod generating telemetry)
	// This allows scenarios to target specific resources or resource types
	Resource ResourceReference `json:"resource"`

	// Timestamp is when this input was created
	// time.Time in Go: includes location, serializes to RFC3339 in JSON
	Timestamp time.Time `json:"timestamp"`

	// Data contains the detection-specific data (unstructured, flexible)
	// map[string]interface{} = key-value pairs where values can be any type
	//
	// SCHEMA-DRIVEN: This is the data that CEL expressions evaluate against
	//
	// For Kubernetes sources, typically contains:
	//   - "status": full resource status object
	//   - "spec": full resource spec object
	//   - "metadata": resource metadata (labels, annotations)
	//
	// For OTel metrics:
	//   - "metricName": metric name (e.g., "http.server.duration")
	//   - "value": current value
	//   - "labels": metric labels/dimensions
	//   - "aggregation": type (avg, sum, rate)
	//
	// For OTel logs:
	//   - "severity": log severity (INFO, ERROR, etc.)
	//   - "body": log message text
	//   - "attributes": log attributes/fields
	//
	// For OTel traces:
	//   - "spanName": name of the span
	//   - "duration": span duration
	//   - "status": span status (OK, ERROR)
	//   - "attributes": span attributes
	// Data holds the raw payload; CEL expressions evaluate against it.
	Data map[string]interface{} `json:"data"`

	// Labels are additional metadata labels for filtering and routing
	// Prometheus-style key-value labels for flexible matching
	//
	// Common labels:
	//   - "eventType": "create", "update", "delete" (K8s events)
	//   - "telemetryType": "metric", "log", "trace" (OTel type)
	//   - "field": which field changed (for K8s updates)
	//   - Custom labels from resource labels or scenario configuration
	//
	// Scenarios can filter on labels before evaluating CEL conditions
	// Example: Only evaluate if labels["eventType"] == "update"
	//
	// omitempty: don't serialize if nil or empty map
	Labels map[string]string `json:"labels,omitempty"`
}

// DetectionResult is the output from Detection Layer to Remediation Layer
// This is emitted when a scenario's conditions and threshold are met
//
// Flow: Detection Engine → DetectionResult → Remediation Controller
type DetectionResult struct {
	// ID is a unique identifier for this result
	// Different from DetectionInput.ID (one input may trigger multiple scenarios)
	ID string `json:"id"`

	// ScenarioID is the ID of the matched scenario
	// References the scenario definition from ScenarioLibrary CRD
	// Example: "S1001" (CrashLoopBackOff scenario)
	ScenarioID string `json:"scenarioId"`

	// ScenarioName is the human-readable name of the scenario
	// For logging and notifications: "CrashLoopBackOff", "HighErrorRate"
	ScenarioName string `json:"scenarioName"`

	// Resource is the affected Kubernetes resource
	// The resource that needs remediation
	Resource ResourceReference `json:"resource"`

	// Severity of the detected issue
	// Determines escalation urgency and notification channels
	Severity Severity `json:"severity"`

	// FirstDetected is when this issue was first detected
	// Used to understand how long a problem has persisted
	FirstDetected time.Time `json:"firstDetected"`

	// LastDetected is when this issue was most recently detected
	// Updated each time the condition matches (for tracking frequency)
	LastDetected time.Time `json:"lastDetected"`

	// DetectionCount is how many times this has been detected
	// Within the scenario's time window (e.g., "3 times in 5 minutes")
	DetectionCount int `json:"detectionCount"`

	// ThresholdMet indicates if the detection threshold was met
	// true = ready for remediation; false = still counting
	ThresholdMet bool `json:"thresholdMet"`

	// Message is a human-readable description
	// Examples: "Pod crashed 3 times in 5 minutes", "Error rate exceeded 5%"
	Message string `json:"message"`

	// Context contains additional context for remediation
	// May include: original detection data, aggregated metrics, previous states
	// Helps remediation layer make smarter decisions
	Context map[string]interface{} `json:"context,omitempty"`

	// RecommendedActions are the actions to take
	// Comes from scenario definition, may be ordered
	// Remediation layer will execute these (after cooldown check)
	RecommendedActions []ActionConfig `json:"recommendedActions"`
}

