// OwnerInfo describes the owner of a resource (e.g. Pod → ReplicaSet → Deployment). Used to resolve the workload for remediation.
type OwnerInfo struct {
	// APIVersion of the owner
	// Examples: "apps/v1", "batch/v1", custom API groups
	APIVersion string `json:"apiVersion"`

	// Kind of the owner (e.g., "ReplicaSet", "Deployment", "StatefulSet")
	Kind string `json:"kind"`

	// Name of the owner
	Name string `json:"name"`

	// UID of the owner (unique identifier)
	UID string `json:"uid"`
}

// ToOwnerReference returns the K8s OwnerReference for this owner.
func (o OwnerInfo) ToOwnerReference() metav1.OwnerReference {
	return metav1.OwnerReference{
		APIVersion: o.APIVersion,
		Kind:       o.Kind,
		Name:       o.Name,
		UID:        types.UID(o.UID), // Convert string to types.UID
	}
}

// ============================================================================
// SCHEMA-DRIVEN ARCHITECTURE NOTES
// ============================================================================
//
// HOW DETECTION WORKS (without DetectionType enum):
//
// 1. MONITOR LAYER (k8s_monitor.go, otel_receiver.go):
//    - Watches resources (Pods, Deployments, Events, etc.)
//    - Converts K8s objects to DetectionInput with full data in Data map
//    - No need to know which scenarios exist
//
// 2. DETECTION LAYER (detection/engine.go):
//    - Loads scenarios from ScenarioLibrary CRDs
//    - For each DetectionInput, finds matching scenarios by:
//      a. Source match (kubernetes vs otel)
//      b. Resource kind match (Pod, Deployment, etc.)
//      c. Label match (optional filters)
//    - Evaluates CEL expression against DetectionInput.Data
//    - Tracks state and threshold
//
// 3. EXAMPLE SCENARIO EVALUATION:
//
//    Scenario (from CRD):
//      detection:
//        source: kubernetes
//        resource: {kind: Pod}
//        condition: |
//          has(data.status.containerStatuses) &&
//          data.status.containerStatuses.exists(cs,
//            has(cs.state.waiting) &&
//            cs.state.waiting.reason == "CrashLoopBackOff"
//          )
//
//    DetectionInput:
//      {
//        "source": "kubernetes",
//        "resource": {kind: "Pod", name: "my-pod"},
//        "data": {
//          "status": {
//            "containerStatuses": [{
//              "state": {"waiting": {"reason": "CrashLoopBackOff"}}
//            }]
//          }
//        }
//      }
//
//    CEL Evaluation:
//      - Engine compiles CEL expression from scenario
//      - Evaluates expression with DetectionInput.Data as "data" variable
//      - Returns true if condition matches
//      - Updates DetectionState, checks threshold
//      - Emits DetectionResult if threshold met
//
// BENEFITS OF THIS APPROACH:
//   + No code changes to add new resource types (just apply CRD)
//   + Works with any Kubernetes resource including CRDs
//   + User-defined scenarios (ops teams can define their own)
//   + GitOps-friendly (scenarios in Git, applied via CI/CD)
//   + Flexible conditions (CEL is powerful expression language)
//   + Runtime validation (CEL expressions validated at scenario load)
//
// TRADEOFFS:
//   - Less compile-time safety (CEL errors at runtime)
//   - Slightly slower (CEL interpretation vs native Go)
//   - Requires CEL knowledge for scenario authors
//   - Debugging CEL expressions harder than Go code
//
// MITIGATION:
//   + Validate CEL at scenario load time (fail fast)
//   + Provide CEL helper functions (hasRecentDeployment, etc.)
//   + Comprehensive CEL error messages with context
//   + Example scenarios as templates
//   + Testing framework for scenarios
